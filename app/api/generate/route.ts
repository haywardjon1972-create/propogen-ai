import { NextRequest, NextResponse } from "next/server";
import { isProUser } from "@/lib/stripe";

type GenerateBody = {
  template?: string;
  tone?: string;
  length?: string;
  company?: string;
  client?: string;
  topic?: string;
  details?: string;
};

const TEMPLATE_LABELS: Record<string, string> = {
  business: "Business Proposal",
  project: "Project Proposal",
  sales: "Sales Proposal",
  report: "Executive Report",
  rfp: "RFP Response",
};

