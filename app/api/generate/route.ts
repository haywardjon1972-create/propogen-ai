async function generateWithXai(body: Required<GenerateBody>): Promise<string | null> {
  const apiKey = process.env.XAI_API_KEY;
  if (!apiKey) return null;

  const templateLabel = TEMPLATE_LABELS[body.template] ?? "Business Proposal";
  const lengthHint =
    body.length === "short"
      ? "Keep it concise (about 400–600 words)."
      : body.length === "long"
        ? "Make it detailed (about 1200–1800 words)."
        : "Aim for a medium length (about 700–1000 words).";

  const prompt = `You are Propogen AI, an expert business writer. Write a polished ${templateLabel} with a ${body.tone} tone.

Company (author): ${body.company}
Client / audience: ${body.client}
Topic / title: ${body.topic}
Key details: ${body.details}

${lengthHint}

Use clear section headings, professional language, and actionable next steps. Output plain text only (no markdown code fences).`;

  try {
    const res = await fetch("https://api.x.ai/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${apiKey}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "grok-4.5",
        messages: [
          {
            role: "system",
            content:
              "You write professional business proposals and documents. Be clear, credible, and well-structured.",
          },
          { role: "user", content: prompt },
        ],
        temperature: 0.7,
      }),
    });

    if (!res.ok) {
      console.error("xAI error:", await res.text());
      return null;
    }

    const data = (await res.json()) as {
      choices?: { message?: { content?: string } }[];
    };
    const text = data.choices?.[0]?.message?.content?.trim();
    return text || null;
  } catch (err) {
    console.error("xAI request failed:", err);
    return null;
  }
}

export async function POST(req: NextRequest) {
  let body: GenerateBody;
  try {
    body = await req.json();
  } catch {
    return NextResponse.json({ error: "Invalid JSON body" }, { status: 400 });
  }

  const company = body.company?.trim() ?? "";
  const client = body.client?.trim() ?? "";
  const topic = body.topic?.trim() ?? "";
  const details = body.details?.trim() ?? "";

  if (!company || !client || !topic || !details) {
    return NextResponse.json(
      { error: "Please fill in company, client, topic, and details." },
      { status: 400 },
    );
  }

  const payload: Required<GenerateBody> = {
    template: body.template || "business",
    tone: body.tone || "professional",
    length: body.length || "medium",
    company,
    client,
    topic,
    details,
  };

  const pro = await isProUser();

  // Live AI is a Pro feature (requires successful Stripe checkout cookie + XAI key)
  if (pro) {
    const aiDoc = await generateWithXai(payload);
    if (aiDoc) {
      return NextResponse.json({
        document: aiDoc,
        source: "Grok · Pro",
        isPro: true,
      });
    }
    return NextResponse.json({
      document: buildMockDocument(payload),
      source:
        "Pro unlocked, but AI unavailable (check XAI_API_KEY) — demo template used",
      isPro: true,
    });
  }

  return NextResponse.json({
    document: buildMockDocument(payload),
    source: "Free demo template · Upgrade to Pro for live AI",
    isPro: false,
  });
}
